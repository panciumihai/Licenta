                >Dreptul de cazare în căminele Universităţii</h2>
                <p>Dreptul de cazare se acordă studenţilor şi doctoranzilor înmatriculaţi la zi care nu au domiciliul stabil în municipiul Iaşi. Nu contează dacă eşti înmatriculat pe locurile de la buget sau cu taxă. Criteriul principal în acordarea dreptului de cazare este media anuală obţinută în anul universitar anterior. Pentru studenţii înmatriculaţi în anul I se va lua în considerare media obţinută la admitere.</p>
                <p>Acordarea dreptului de cazare se realizează de către Comisia de cazare a fiecărei facultăţi, din care fac parte obligatoriu, şi reprezentanţi ai studenţilor în Consiliul facultăţii.</p>
                <p>La cazare au prioritate studenţii orfani de ambii părinţi, cei care beneficiază de măsură de protecție socială-plasament, dacă au obţinut minimum 20 de credite pentru obligaţiile didactice din anul universitar anterior. Refuzul repartiţiei de cazare sau renunţarea la cazare în timpul anului universitar conduce la pierderea dreptului de cazare pentru anul universitar în curs.</p>
                <p>Studenţii bugetari care tranzacţionează dreptul de cazare vor fi sancţionaţi cu exmatricularea; studenţii cu taxă care tranzacţionează dreptul de cazare vor fi sancţionaţi cu exmatricularea şi pierderea dreptului de reînmatriculare la UAIC.</p>
